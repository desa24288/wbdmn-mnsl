    CodEstadoBono?: string,
    GloEstadoBono?: string,
    CodOrigenVenta?: string,
    GloOrigenVenta?: string,
    FechaMovBono?: Date,
    RutCotizante?: string,
    NombreCotizante?: string,
    RutPaciente?: string,
    NombrePaciente?: string,
    RutPrestador?: string,
    NombrePrestador?: string,
    TotMontoPresta?: string,
